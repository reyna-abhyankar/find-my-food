    override func tableView(tableView: UITableView, willDisplayCell cell: UITableViewCell, forRowAtIndexPath indexPath: NSIndexPath) {
        if !preventAnimation.contains(indexPath) {
            preventAnimation.insert(indexPath)
            CellAnimator.animateTableCell(cell)
        }
    }
    
    override func tableView(tableView: UITableView, heightForRowAtIndexPath indexPath: NSIndexPath) -> CGFloat {
        return tableView.rowHeight
    }

    override func numberOfSectionsInTableView(tableView: UITableView) -> Int {
        return 1
    }

    override func tableView(tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return array.count
    }

    
    override func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCellWithIdentifier("restaurantCard", forIndexPath: indexPath) as! TableViewCell

        // Configure the cell...
        let restaurant = array[indexPath.row]

        cell.restaurantImage.image = restaurant.businessImage
        
        cell.nameLabel.text = restaurant.businessName
        cell.addressView.text = restaurant.businessLocationDisplayAddress
        
        cell.ratingImage.image = restaurant.businessRatingImage

        return cell
    }
    
